package hw0;

public class Creative_Exercise_1a {
    public void exec(){
        int col=0;
        int row=0;
        int size=5;
        while (row<size){
            col=0;
            while (col<=row){
                System.out.print("*");
                col++;
            }
            System.out.println();
            row++;
        }


    }









}



